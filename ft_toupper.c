#include "libft.h"

int	ft_toupper(int c)
{
	if (c != '\0')
	{
		if (c >= 'a' && c <= 'z')
		{
			c = (char) (c - ('a' - 'A'));
			return (c);
		}
		else if (c != 'a' && c != 'z')
		{
			return (c);
		}
	}
	return(0);
}
