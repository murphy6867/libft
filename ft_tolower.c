#include "libft.h"

int	ft_tolower(int c)
{
	if (c != '\0')
	{
		if (c >= 'A' && c <= 'Z')
		{
			c = (char) (c - ('A' - 'a'));
			return (c);
		}
		else if (c != 'A' && c != 'Z')
		{
			return (c);
		}
	}
	return(0);
}
