#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	unsigned char *strsrc;
	unsigned char *strdest;
	size_t i;
	
	strsrc = (unsigned char*)src;
	strdest = (unsigned char*)dest;
	i = 0;
	while (i < n)
	{
		strdest[i] = strsrc[i];
		i++;
	}
	return (dest);
}
